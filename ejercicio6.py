numero = int(input('Ingresa un numero: '))

if numero >= 0 and numero <= 10 :
    print("El numero esta entre 0 y 10")
elif numero >= 11 and numero <= 20:
    print("El numero esta entre 11 y 20")
elif numero >= 21 and numero <= 30:
    print("El numero esta entre 21 y 30")
else:
    print("El numero no esta en ese rango")
